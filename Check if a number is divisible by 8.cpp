class Solution{
    public:
    int DivisibleByEight(string s){
        int minus = s.length() >= 3 ? 3: s.length();
        long long num = stoi(s.substr(s.length()-minus,minus));
        if(num%8 ==0) return 1;
        return -1;
    }
};

