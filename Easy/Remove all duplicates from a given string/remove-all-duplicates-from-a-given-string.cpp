//User function template for C++
class Solution{
public:
	string removeDuplicates(string str) {
	    set<char> s;
	    string ans;
	    for(char i:str){
	        int ln = s.size();
	        s.insert(s.begin(),i);
	        if(s.size()!= ln){
	            ans+=i;
	        }
	    }
	    return ans;
	}
};

