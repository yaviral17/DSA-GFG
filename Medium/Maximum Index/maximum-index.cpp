class Solution{
    public:
        
    // A[]: input array
    // N: size of array
    // Function to find the maximum index difference.
    int maxIndexDiff(int a[], int n) 
    { 
        int j =n-1;
        int max_distance = 0;
        for(int i = 0;i<n;j--){
            if(i>j){
                j=n;

                i++;
            }
            if(a[i]<=a[j]){
                max_distance = max(max_distance,j-i);
                j=n;
                i++;
            }
        }
        return max_distance;
    }
};

